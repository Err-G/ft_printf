#include "ft_printf.h"

int	ft_eval_wid(t_pa *pa, const char *str)
{
	int	res;
	int	i;

	res = 0;
	i = -1;
	while (ft_strchr("0123456789", str[++i]))
		res = (res * 10) + (ft_strchr("0123456789", str[i]) - "0123456789");
	pa->wid = res;
	return (i);
}
